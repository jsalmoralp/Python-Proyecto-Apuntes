# Las variables no necesitan ser obligadamente declaradas con el tipo de valor que van a tener.
nombre = "Joan Salmoral"
print(nombre)

# Las variables pueden ser de cualquier tipo.
edad = 25
print(edad)

# Si una variable cambia de tipo de valor, python lo acepta y recoge el nuevo valor.
edad = True
print(edad)

# Las variables pueden sr incluso de tipo decimal.
sueldo = 205.10
print(sueldo)
