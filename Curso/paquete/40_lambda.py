####
# Definicion de una función normal.
####
def sumar(n1, n2):
    return n1 + n2


print(sumar(12, 15))
# [FIN] Definicion de una función normal.

####
# Definicion de una función lambda.
####
"""
Funciones Lambda: Son funciones anónimas que sirven para abreviar o resumir una función normal,
para convertirla en una expresión simple.
Toda función lamba se puede convertir a una función normal, pero no viceversa.
"""
multiplicar = lambda n1, n2: n1 * n2

print(multiplicar(2, 2))
# [FIN] Definicion de una función lambda.
